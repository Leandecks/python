nrg = int(input("Inserisci il numero del giorno della settimana: "))

if nrg ==1 :
    print("È lunedì")
elif nrg==2:
    print("È martedì")
elif nrg==3:
    print("È mercoledì")
elif nrg==4:
    print("È giovedì")
elif nrg==5:
    print("È venerdì")
elif nrg==6:
    print("È sabato")
elif nrg==7:
    print("È domenica")
else:
    print("Non sai contare")
